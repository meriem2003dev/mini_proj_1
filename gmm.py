"""# Gaussian"""

def gaussian_pdf(x, mean, cov):

    n = len(mean)

    cov = cov + np.eye(n) * 1e-6

    det = np.linalg.det(cov)

    inv = np.linalg.inv(cov)

    norm_const = 1.0 / (
        np.power((2 * np.pi), n / 2) *
        np.sqrt(det)
    )

    x_mu = x - mean

    result = np.exp(
        -0.5 * np.dot(
            np.dot(x_mu, inv),
            x_mu.T
        )
    )

    return norm_const * result

