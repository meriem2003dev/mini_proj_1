    # M-Step
    means, covariances, pis = maximization_step(
        features,
        responsibilities
    )

    # حساب Log Likelihood
    ll = 0

    for x in features:

        temp = 0

        for k in range(K):

            temp += (
                pis[k] *
                gaussian_pdf(
                    x,
                    means[k],
                    covariances[k]
                )
            )

        ll += np.log(temp + 1e-6)

    log_likelihoods.append(ll)

    print(
        f"Epoch {epoch+1}, Log Likelihood: {ll}"
    )

